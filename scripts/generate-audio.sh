#!/bin/bash

###############################################
# Part 1 录制配音自动生成脚本
# 使用macOS内置say命令生成高质量中文语音
###############################################

# 输出目录
OUTPUT_DIR="docs/audio/part1"
mkdir -p "$OUTPUT_DIR"

echo "🎙️ 开始生成Part 1配音..."
echo "📁 保存位置: $OUTPUT_DIR"
echo ""

# 设置中文语音（使用Ting-Ting - 高质量中文女声）
VOICE="Ting-Ting"

# 检查是否有可用的中文语音
if ! say -v "$VOICE" "测试" 2>/dev/null; then
    echo "⚠️  未找到中文语音 $VOICE，使用默认语音"
    VOICE="Mei-Jia"  # 备用中文语音
fi

echo "使用语音: $VOICE"
echo ""

# 00:00-00:10 开场白
echo "🎤 [1/13] 生成开场白..."
say -v "$VOICE" -o "$OUTPUT_DIR/01-opening.aiff" \
"商户智运Agent，让商场运营更智能。今天我将展示项目总监如何使用系统掌控全局、数据驱动决策。"

# 00:10-00:50 场景1.1 首页仪表板
echo "🎤 [2/13] 生成首页仪表板旁白..."
say -v "$VOICE" -o "$OUTPUT_DIR/02-dashboard.aiff" \
"每周一早晨8点，李总监打开商户智运系统。首页一目了然：18户商户中，有2户处于高风险状态，需要立即关注。平均健康度78分，整体运营稳定。"

# 饼图和趋势图
echo "🎤 [3/13] 生成健康度分布旁白..."
say -v "$VOICE" -o "$OUTPUT_DIR/03-charts.aiff" \
"饼图显示，11%的商户处于高风险，17%处于中风险。趋势图显示近6个月风险商户数量呈下降趋势，说明帮扶措施有效。"

# 00:50-01:30 场景1.2 商户详情
echo "🎤 [4/13] 生成商户详情旁白..."
say -v "$VOICE" -o "$OUTPUT_DIR/04-merchant-detail.aiff" \
"李总监点击海底捞火锅，详情立即显示。健康度仅45分，处于高风险。五维指标中，经营表现和抗风险能力最弱，只有35分。"

echo "🎤 [5/13] 生成AI诊断提示..."
say -v "$VOICE" -o "$OUTPUT_DIR/05-ai-prompt.aiff" \
"点击AI诊断按钮，系统快速生成报告。"

# 01:30-02:20 场景2.1 AI诊断
echo "🎤 [6/13] 生成AI诊断旁白..."
say -v "$VOICE" -o "$OUTPUT_DIR/06-ai-diagnosis.aiff" \
"AI诊断快速生成报告。系统发现核心问题：营收持续下滑18%、租金拖欠45万、翻台率低于行业标准。更重要的是，系统自动匹配了成功案例——蜀大侠火锅的帮扶经验，3个月内营收提升25%。"

# 02:20-03:00 场景2.2 创建任务
echo "🎤 [7/13] 生成创建任务旁白..."
say -v "$VOICE" -o "$OUTPUT_DIR/07-create-task.aiff" \
"李总监点击'一键创建任务'，商户信息和AI推荐的4条措施已自动填充。他指定张经理为责任人，点击保存。任务立即创建完成，张经理的手机同步收到了任务通知。"

# 03:00-03:20 场景3.1 健康度监控
echo "🎤 [8/13] 生成健康度监控旁白..."
say -v "$VOICE" -o "$OUTPUT_DIR/08-health-monitoring.aiff" \
"进入健康度监控，18户商户一览无余。周生生、周大福等珠宝店表现优异，而海底捞排在末尾。"

# 03:20-04:30 场景3.2 商户对比
echo "🎤 [9/13] 生成商户对比介绍..."
say -v "$VOICE" -o "$OUTPUT_DIR/09-compare-intro.aiff" \
"李总监选择同类型的三家火锅店进行对比。雷达图清晰显示，海底捞在所有维度都明显落后。"

echo "🎤 [10/13] 生成商户对比数据..."
say -v "$VOICE" -o "$OUTPUT_DIR/10-compare-data.aiff" \
"对比表格进一步揭示：小龙坎翻台率3.8次，而海底捞仅2.1次，仅为标杆的55%。这些数据为帮扶方向提供了明确依据。"

# 04:30-05:00 场景4.1 帮扶档案
echo "🎤 [11/13] 生成帮扶档案旁白..."
say -v "$VOICE" -o "$OUTPUT_DIR/11-archive.aiff" \
"以蜀大侠火锅为例，这是一个成功的帮扶案例。档案摘要显示，共有8次历史记录，3次风险等级变更，2个帮扶任务，成功率100%。"

# 05:00-05:30 场景4.2 健康度趋势
echo "🎤 [12/13] 生成健康度趋势旁白..."
say -v "$VOICE" -o "$OUTPUT_DIR/12-health-trend.aiff" \
"健康度趋势图展示了完整的帮扶过程。起始58分，中风险，经过两轮帮扶措施后，评分稳步提升至75分，低风险。每个关键节点都有清晰标注，效果一目了然。"

# 05:30-06:00 场景5.1 任务清单
echo "🎤 [13/13] 生成任务清单旁白..."
say -v "$VOICE" -o "$OUTPUT_DIR/13-task-measures.aiff" \
"展开任务详情，每条措施的效果清晰可见。措施A'优化菜单结构'效果显著，经营表现从58分提升到68分，新菜品毛利率45%，营收增长18%。措施B'联合营销'效果中等，客流提升但转化不足。"

# 06:00-06:30 场景5.2 措施排行榜
echo "🎤 生成措施排行榜旁白..."
say -v "$VOICE" -o "$OUTPUT_DIR/14-ranking.aiff" \
"系统自动统计所有商户的措施效果，生成排行榜。'菜单结构优化'效果最佳，平均改善12.5分，已成功应用5次。这些数据帮助李总监快速决策——同类问题优先使用高效措施。"

# 06:30-06:40 结尾
echo "🎤 生成结尾..."
say -v "$VOICE" -o "$OUTPUT_DIR/15-ending.aiff" \
"项目总监通过商户智运Agent，掌控全局，数据驱动决策，让商户运营更高效。"

echo ""
echo "✅ 所有配音生成完成！"
echo "📁 位置: $OUTPUT_DIR"
echo ""
echo "🎧 试听第一段配音:"
echo "afplay \"$OUTPUT_DIR/01-opening.aiff\""
echo ""
echo "📋 配音文件列表:"
ls -lh "$OUTPUT_DIR"/*.aiff | awk '{print $9, "(" $5 ")"}'
echo ""
echo "💡 使用方法:"
echo "1. 试听: afplay docs/audio/part1/01-opening.aiff"
echo "2. 录制时播放对应配音，同步操作"
echo "3. 或后期导入视频编辑软件"
